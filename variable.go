package main

import "fmt"

func main() {
	var name string

	name = "Gera Anggara"
	fmt.Println(name)

	name = "Anggara"
	fmt.Println(name)

	var kota = "Bandung"
	fmt.Println(kota)

	var age = 17
	fmt.Println(age)

	country := "Indonesia" // deklarasi variable tanpa var
	fmt.Println(country)

	var (
		firstName = "Gera"
		lastName = "Anggara"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}

